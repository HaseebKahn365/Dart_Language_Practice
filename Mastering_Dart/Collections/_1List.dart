void main() {
  var cities = ['karachi', 'lahore', 'islamabad', 'peshawar'];
  print(cities);
  print(cities[1]);
  for (var any in cities) {
    print(any);
  }
  var sum = 0;
  var nums = [1, 2, 3, 4, 5];
  print(nums[1]);
  for (var i in nums) {
    sum += i;
  }
  print(sum);
}

